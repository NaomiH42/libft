#include "libft.h"

void	*memchr(const void *s, int c, size_t n)
{
	unsigned int	i;

	i = 0;
	while (i < n)
	{
		if (*(unsigned char *)s == c)
			return (s);
		s++;
		i++;
	}
	return (NULL);
}
