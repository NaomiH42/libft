#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*mem;

	mem = malloc(size);
	if (!mem)
		return (NULL);
	size--;
	while (size)
	{
		mem[size] = 0;
		size--;
	}
	return (mem);
}
